package Patterns;

import java.util.*;
public class NumbertrianglePattern {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int t=1;
		 
		while(n>0) {
		 
			for(int i=0;i<t;i++) {
                
				System.out.print(t);
			}
			System.out.println();
			t++;
			n--;
	}
		}

	}


